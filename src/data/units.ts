//   Scorch : +10% weapon damage, -10% speed
//   Tide   : +10% speed, naval 15% cheaper
//   Verdant: infantry 15% cheaper (out-of-combat regen handled in sim/combat.ts
//            by faction check — not expressible in UnitDef)
