import { ArmorClass, Element, MoveDomain, WeaponClass } from '../core/types';
import type { UnitDef, WeaponDef } from '../core/types';
import { secondsToTicks } from '../core/constants';

// buildTicks default per DESIGN.md
const bt = (cost: number): number => secondsToTicks(cost / 30);
// round helpers (quirk math)
const r5 = (x: number): number => Math.round(x / 5) * 5;
const r1 = (x: number): number => Math.round(x * 10) / 10;
// Scorch: +10% damage, -10% speed
const sDmg = (base: number): number => Math.max(1, Math.round(base * 1.1));
const sSpd = (base: number): number => r1(base * 0.9);
// Tide: +10% speed, naval -15% cost
const tSpd = (base: number): number => r1(base * 1.1);
const tNavCost = (base: number): number => r5(base * 0.85);
// Verdant: infantry -15% cost
const vInfCost = (base: number): number => r5(base * 0.85);

interface WeaponSpec {
  damage: number;
  range: number;
  cooldown: number;
  weaponClass: WeaponClass;
  projectileSpeed: number;
  splashRadius?: number;
  canTargetGround?: boolean;
  canTargetAir?: boolean;
  burst?: number;
}

function wpn(w: WeaponSpec): WeaponDef {
  return {
    damage: w.damage,
    range: w.range,
    cooldown: w.cooldown,
    weaponClass: w.weaponClass,
    projectileSpeed: w.projectileSpeed,
    splashRadius: w.splashRadius ?? 0,
    canTargetGround: w.canTargetGround ?? true,
    canTargetAir: w.canTargetAir ?? false,
    ...(w.burst !== undefined ? { burst: w.burst } : {}),
  };
}

type UnitSpec = Omit<UnitDef, 'buildTicks' | 'spriteKey' | 'uiOrder'>;

// spriteKey = id, buildTicks = secondsToTicks(cost/30),
// uiOrder ascending by tier then cost.
function mk(spec: UnitSpec): UnitDef {
  return {
    ...spec,
    buildTicks: bt(spec.cost),
    spriteKey: spec.id,
    uiOrder: spec.tier * 10000 + spec.cost,
  };
}

